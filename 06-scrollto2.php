$('a[href^="#"]').on('click', function(e) {
	e.preventDefault();

	var link = $(this).index() + 1;
	var background = "";
	var pos = $(this.hash).offset().top - 75;

	if (link == 1) {
		background = "#F00" //red
	} else if (link == 2) {
		background = "#FF5000" //orange
	} else if (link == 3) {
		background = "#FF0" //yellow
	} else if (link == 4) {
		background = "#0F0" //green
