		background = "#0FF" //light blue
	} else if (link == 6) {
		background = "#00F" //dark blue
	} else if (link == 7) {
		background = "#F0F" //fuschia
	}

	$('body, html').stop().animate({scrollTop: pos, backgroundColor: background}, 1000, 'easeInOutQuart');

})


